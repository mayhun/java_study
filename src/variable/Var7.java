package variable;

// 변수 타입
// 변수를 데이터를 다루는 종류에 따라 다양한 형식이 존재.
// 이러한 형식을 영어로는 타입(type)이라고하고, 우리말로는 형식 또는 형 이라 한다.

public class Var7 {
    public static void main(String[] args) {
        int a = 10; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언(boolean), true, false 만 입력가능
        char d = 'A'; // 문자 하나
        String e = "Hello Java"; // 문자열, 문자열을 다루기 위한 특별한 타입
                                // String 은 첫글자가 대문자로 시작하는 특별한 타입. 이 부분은 클래스를 배워야 자세히 이해할 수 있다.


        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
// 리터럴
